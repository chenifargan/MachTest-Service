package com.example.finalproject;

public class MatchBoundary {
    private Person p1;
    private Person p2;

    public MatchBoundary(Person p1, Person p2) {
        this.p1 = p1;
        this.p2 = p2;
    }

    public Person getP1() {
        return p1;
    }

    public void setP1(Person p1) {
        this.p1 = p1;
    }

    public Person getP2() {
        return p2;
    }

    public void setP2(Person p2) {
        this.p2 = p2;
    }
}
